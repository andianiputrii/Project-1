    private void HitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HitungActionPerformed
        // TODO add your handling code here:
        String rumus;
        rumus="";
        if(CBKeliling.isSelected() && CBLuas.isSelected() && CBVolume.isSelected()){
            // LUAS
            sisi = Double.parseDouble(this.TfSisi.getText());
            luas = 6 * sisi * sisi;
            TfLuas.setText(String.valueOf(luas));
            // KELILING
            sisi = Double.parseDouble(this.TfSisi.getText());
            keliling = 12 * sisi;
            TfKeliling.setText(String.valueOf(keliling));
            // VOLUME
            sisi = Double.parseDouble(this.TfSisi.getText());
            volume = sisi * sisi * sisi;
            TfVolume.setText(String.valueOf(volume));
            
        }
        else if(CBKeliling.isSelected() && CBLuas.isSelected()){
            // LUAS
            sisi = Double.parseDouble(this.TfSisi.getText());
            luas = 6 * sisi * sisi;
            TfLuas.setText(String.valueOf(luas));
            // KELILING
            sisi = Double.parseDouble(this.TfSisi.getText());
            keliling = 12 * sisi;
            TfKeliling.setText(String.valueOf(keliling));
        }
        else if (CBLuas.isSelected() && CBVolume.isSelected()){
         // LUAS
            sisi = Double.parseDouble(this.TfSisi.getText());
            luas = 6 * sisi * sisi;
            TfLuas.setText(String.valueOf(luas));
            // VOLUME
            sisi = Double.parseDouble(this.TfSisi.getText());
            volume = sisi * sisi * sisi;
            TfVolume.setText(String.valueOf(volume));   
        }
        else if (CBKeliling.isSelected() && CBVolume.isSelected()){
            // KELILING
            sisi = Double.parseDouble(this.TfSisi.getText());
            keliling = 12 * sisi;
            TfKeliling.setText(String.valueOf(keliling));
            // VOLUME
            sisi = Double.parseDouble(this.TfSisi.getText());
            volume = sisi * sisi * sisi;
            TfVolume.setText(String.valueOf(volume));
        }
        else if (CBLuas.isSelected()) {
            sisi = Double.parseDouble(this.TfSisi.getText());
            luas = 6 * sisi * sisi;
            TfLuas.setText(String.valueOf(luas));
        }
        else if(CBKeliling.isSelected()) {
            sisi = Double.parseDouble(this.TfSisi.getText());
            keliling = 12 * sisi;
            TfKeliling.setText(String.valueOf(keliling));
        }
        else if(CBVolume.isSelected()) {
            sisi = Double.parseDouble(this.TfSisi.getText());
            volume = sisi * sisi * sisi;
            TfVolume.setText(String.valueOf(volume));
        }
        else if (rumus.equals("")) {
            JOptionPane.showMessageDialog(null, 
                              "Tidak Boleh Kosong", 
                              "Alert", 
                              JOptionPane.WARNING_MESSAGE);
        }
        else {
            rumus = rumus.substring(0, rumus.length()-2) + ".";
            TfLuas.setText(String.valueOf(luas));
        }
        
    }//GEN-LAST:event_HitungActionPerformed

