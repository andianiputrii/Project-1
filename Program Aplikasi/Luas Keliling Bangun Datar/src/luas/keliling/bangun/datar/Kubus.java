    private void TfSisiKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TfSisiKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (!((Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE)))) {
            evt.consume();
        }
    }//GEN-LAST:event_TfSisiKeyTyped

